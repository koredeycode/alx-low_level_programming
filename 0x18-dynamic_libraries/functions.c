/**
 * add - add two integers
 * @a: int a
 * @b: int b
 * Return: return a + b
 */
int add(int a, int b)
{
	return (a + b);
}
/**
 * sub - subtract two integers
 * @a: int a
 * @b: int b
 * Return: return a - b
 */
int sub(int a, int b)
{
	return (a - b);
}
/**
 * mul - multiply two integers
 * @a: int a
 * @b: int b
 * Return: return a * b
 */
int mul(int a, int b)
{
	return (a * b);
}
/**
 * div - add two integers
 * @a: int a
 * @b: int b
 * Return: return a / b
 */
int div(int a, int b)
{
	return (a / b);
}
/**
 * mod - find the mod between two integers
 * @a: int a
 * @b: int b
 * Return: return a % b
 */
int mod(int a, int b)
{
	return (a % b);
}
